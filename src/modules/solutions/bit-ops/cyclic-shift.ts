// Циклический сдвиг влево
// Биты, "выпавшие" слева, возвращаются справа
export function cyclicLeftShift(value: number, shift: number): number {
  // Убираем полные циклы: сдвиг на 32 бита эквивалентен 0,
  // поэтому берём остаток от деления на 32 (реальный сдвиг 0–31)
  shift %= 32;

  // Сдвиг на 0 — возвращаем исходное значение
  if (shift === 0) {
    return value;
  }

  /**
   * Основной сдвиг влево
   *
   * Пример:
   * при value = 10110011 01010101 11110000 00001111 и shift = 4
   *
   * до:         1011 0011 0101 0101 1111 0000 0000 1111
   * после << 4: 0011 0101 0101 1111 0000 0000 1111 0000
   *
   * все биты сместились влево на 4 позиции
   * освободившееся место заполнилось нулями,
   * левые биты 1011 вытеснены
   */
  const shifted = value << shift;

  /**
   * Берём биты, которые "вылетят" слева при сдвиге
   * Правый сдвиг с заполнением нулями, чтобы не тянуть знак
   *
   * value >>> 28
   * value: 1011 0011 0101 0101 1111 0000 0000 1111 >>> 28:
   *        0000 0000 0000 0000 0000 0000 0000 1011
   */
  const lostBits = value >>> (32 - shift);

  // Объединяем: добавляем "потерянные" биты справа
  return lostBits | shifted;
}

// Циклический сдвиг вправо
// Биты, "выпавшие" справа, возвращаются слева
export function cyclicRightShift(value: number, shift: number): number {
  // Убираем полные циклы: сдвиг на 32 бита эквивалентен 0,
  // поэтому берём остаток от деления на 32 (реальный сдвиг 0–31)
  shift %= 32;

  // Сдвиг на 0 — возвращаем исходное значение
  if (shift === 0) {
    return value;
  }

  /**
   * Основной сдвиг вправо (без знака)
   *
   * Пример (тот же):
   * при value = 10110011 01010101 11110000 00001111 и shift = 4
   *
   * до:          1011 0011 0101 0101 1111 0000 0000 1111
   * после >>> 4: 0000 1011 0011 0101 0101 1111 0000 0000
   *
   * все биты сместились вправо на 4 позиции,
   * освободившееся место заполнилось нулями,
   * правые 4 бита (1111) были вытеснены
   */
  const shifted = value >>> shift;

  /**
   * Берём биты, которые "вылетят" справа при сдвиге
   * сдвигаем их влево, чтобы вернуть в старшие биты
   *
   * value << 28
   * value: 1011 0011 0101 0101 1111 0000 0000 1111
   *        1111 0000 0000 0000 0000 0000 0000 0000
   */
  const lostBits = value << (32 - shift);

  // Объединяем: добавляем "потерянные" биты слева
  return shifted | lostBits;
}
